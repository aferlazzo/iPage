<dd><a style="font:x-large Times New Roman;margin-right:10em;" name="E">- E -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt>Earnest Money</dt>
<dd>The deposit money given to
                seller or his agent by the potential buyer at the
                time of the purchase offer. If the offer is
                accepted, the money will become part of the down
                payment.</dd>
<dt>Easement</dt>
<dd>A right to the limited use of
                land owned by another. An electric company, for
                example, could have an easement to put up
                electric power lines over someone's property.</dd>
<dt>Encumbrance</dt>
<dd>Anything that affects or
                limits the title to a property, such as
                outstanding mortgages, easement rights or unpaid
                back taxes.</dd>
<dt><a
                class="DT" name="Equity">Equity</a></dt>
<dd>The value in which the owner
                has in real estate over and above the mortgages
                against it. When the mortgage and all other debts
                against the property are paid in full, the owner
                has 100% equity in his property.</dd>
<dt><a
                class="DT" name="Escrow">Escrow</a></dt>
<dd>Funds and/or deed left in
                trust to a third party. Generally, a portion of
                the monthly mortgage payment is held in escrow by
                the lender to pay for taxes, hazard insurance and
                yearly mortgage insurance premiums.
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="F">- F -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt><a name="FannieMae">Fannie
                Mae</a></dt>
<dd>Fannie Mae (<a title="opens a new window"
                class="DD" target="_blank"
                href="http://www.fanniemae.com">FNMA</a><img src="../images/NewWindow.gif" ALT="">: Federal
                National Mortgage Association) is a
                stockholder-owned corporation chartered by
                Congress to increase the supply of funds that
                mortgage lenders, such as commercial banks,
                mortgage bankers, savings institutions and credit
                unions, can make available to homebuyers and
                multifamily investors. See <a
                class="DD"
                href="#FreddieMac">Freddie Mac</a> for further
                information.</dd>
<dt>First Mortgage</dt>
<dd>A mortgage that has a primary
                lien against a property.</dd>
<dt>Fixed-Rate<br />Mortgage</dt>
<dd>A mortgage with an interest
                rate and monthly payments that remain constant
                over the life of the loan. See <a
                class="DD"
                href="http://www.lightning-mortgage.com/LoanTypes/Fixed.php">Our
                Fixed Rates Loan Type</a> page.</dd>
<dt>Fixture</dt>
<dd>Property, such as a hot water
                heater or plumbing fixture, that has become
                permanently attached to piece of real estate and
                goes with the property when it is sold.</dd>
<dt>Flood<br />Certification</dt>
<dd>An independent agency report
                required by the lender to determine whether a
                property is located in a flood hazard zone, which
                would then require a federally mandated flood
                insurance policy.</dd>
<dt>Forbearance</dt>
<dd>A lender's act of refraining
                from taking legal action despite the fact a
                mortgage is in arrears (past due). It's usually
                granted when a mortgagor makes a satisfactory
                arrangement by which the arrears will be paid at
                a future date.</dd>
<dt>Foreclosure</dt>
<dd>A legal procedure in which
                property mortgaged as security for a loan is sold
                to pay the defaulting borrower's debt.</dd>
<dt><a
                class="DD" name="FreddieMac">Freddie Mac</a></dt>
<dd>Freddie Mac (<a title="opens a new window"
                class="DD" target="_blank"
                href="http://www.freddiemac.com">FHLMC</a><img src="../images/NewWindow.gif" ALT="">:
                Federal Home Loan Mortgage Corporation) is a
                stockholder-owned corporation chartered by
                Congress to increase the supply of funds that
                mortgage lenders, such as commercial banks,
                mortgage bankers, savings institutions and credit
                unions, can make available to homebuyers and
                multifamily investors. See <a
                class="DD"
                href="#FannieMae">Fannie Mae</a> for further
                information.<br />
                <br />
                Freddie Mac and Fannie Mae have the same
                charters, Congressional mandates and regulatory
                structure. The two companies, however, have
                different business strategies. Competition
                between Freddie Mac and Fannie Mae ensures that
                the benefits of the secondary market are passed
                on to homebuyers and renters in the form of lower
                housing costs. Both Freddie Mac and Fannie Mae
                operate as publicly traded corporations. See <a
                class="DD" target="_blank" title="opens a new window"
                href="../InterestRates/InterestRateSetting.php">The Cost Of Money</a><img src="../images/NewWindow.gif" ALT="">
                for more information.</dd>

<dt><a
                class="DD" name="Fully Documented Loan">Fully<br />Documented<br />Loan</a></dt>
<dd>Both income and assets are
                disclosed and verified, and income is used in
                determining the applicant's ability to repay the
                mortgage. Formal verification requires the
                borrower's employer to verify employment and the
                borrower's bank to verify deposits. Alternative
                documentation, designed to save time, accepts
                copies of the borrower's original bank
                statements, W-2s and paycheck stubs. See <a
                class="DD"
                href="http://www.lightning-mortgage.com/LoanTypes/IncomeDocVariations.php">Income
                Reporting Options</a> for further information.
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="G">- G -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt>Gift of Equity</dt>
<dd>Often used in conjunction with
                family members helping out with first-time home
                purchasing. A 'gift of equity' is a gift of all
                or part of the <a class="DD"
                href="#Equity">equity</a> in a property you
                currently own to the buyer so that the cash
                requirement for a down payment is smaller or
                eliminated. May have income tax implications.</dd>
<dt>Good Faith<br />Estimate</dt>
<dd>A report that shows the
                estimated costs involved with obtaining a
                mortgage or refinancing. Line-by-line <a
                class="DD"
                href="../Answers/GFEExplained.php">explanation</a>. Compare
                with <a class="DD"
                href="../Answers/HUD1Explained.php">HUD-1</a>.</dd>
<dt><a class="DT"
                name="GNMA">Government<br />National<br />Mortgage<br />Association<br />(GNMA)</a></dt>
<dd>Also known as &quot;Ginnie
                Mae,&quot; provides sources of funds for
                residential mortgages, insured or guaranteed by
                FHA or VA. Compare with <a class="DD"
                href="../Answers/GlossaryAD.php#Conventional-Mortgage">Conventional
                Loan</a>.</dd>
<dt>Graduated<br />Payment<br />Mortgage</dt>
<dd>A fixed rate loan with monthly
                payments that start low, increasing by a fixed
                amount for a specific number of years. After that
                period, the payments typically remain constant
                for the duration of the loan.</dd>
<dt>Gross Income</dt>
<dd>Normal income, including
                overtime, prior to any payroll deductions, that
                is regular and dependable. This income may come
                from more than one source.
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="H">- H -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt>Hazard<br />Insurance</dt>
<dd>Insurance protection against
                damage to a property from fire, windstorms, and
                other common hazards. Often called Homeowner's
                Insurance.</dd>
<dt>Holding Title</dt>
<dd>The legal way that you wish to own
                your property. Also known as vesting. See <a class="DD"
                href="http://www.lightning-mortgage.com/Answers/HoldingTitle.php">The Six
                Most Common Ways of Holding Title</a> page.</dd>
<dt>Home Equity<br />Line Of Credit<br />(HELOC)</dt>
<dd>Gives you the ability to
                utilize the equity in your home to pay off high
                interest credit card debt, finance college
                education costs or make improvements to your
                home.&nbsp; See <a
                class="DD"
                href="http://www.lightning-mortgage.com/LoanTypes/HELOC.php">Our
                HELOC Loan Type</a> page.</dd>
<dt>Homeowner's<br />Insurance</dt>
<dd>An insurance policy that
                covers the dwelling and its contents in case of
                fire or wind damage, theft, liability for
                property damage and personal liability.</dd>
<dt><a
                class="DT" name="HUD-1 Form">HUD-1 Form</a></dt>
<dd>See <a class="DD"
                href="GlossaryQT.php#Real-Estate-Settlement-Statement">Real
                Estate Settlement Statement</a>. A line-by-line <a
                class="DD"
                href="../Answers/HUD1Explained.php">explanation</a>.
