                Impound Accounts</a> for more.</dd>
<dt>Income Property</dt>
<dd>Real estate that is owned for
                investment purposes and not used as the owner's
                residence.</dd>
<dt><a class="DT" name="Index">Index</a></dt>
<dd>The financial instrument used
                as the foundation for determining future rates as
                adjustments are made to <a class="DD"
                href="../LoanTypes/ARM.php">ARMs</a>. There are
                several indexes that are used in the mortgage
                industry--<a class="DD" href="GlossaryQT.php#T-Bill">T-Bill</a>,
                <a class="DD" href="#LIBOR">LIBOR</a>, Prime, <a class="DD"
                href="../Answers/GlossaryAD.php#COFI">COFI</a>, and CODI.<br />
                <br />
                View <a class="DD"
                target="_blank" title="opens a new window"
                href="http://www.hsh.com/idxhst.phpl">charts
                &amp; tables</a><img src="../images/NewWindow.gif" ALT=""> showing the history of the
                different ARM Indexes used to set ARM loan rates.</dd>
<dt>Interest</dt>
<dd>A charge paid to a lender for
                the use of money.</dd>
<dt><a class="DT" name="InterestOnly"></a>Interest Only<br />Payments</dt>
<dd>Control your cash flow with
                interest only loans. Available in both adjustable
                and fixed rate forms these loans allow you to
                have greater purchasing power, reduced monthly
                obligations and many more features not available
                with the traditional 30 year fixed principal and
                interest loan. Interest Only Loans can be tied to
                many Indexes (see 'Index' above).<br />
                See our <a class="DD"
                href="../LoanTypes/InterestOnly.php">Interest-only
                Payments</a> page for more information.</dd>
