<dt>Margin</dt>
<dd>The amount the lender adds to
                the index to arrive at the adjusted rate so as to
                provide a satisfactory yield for his investment.
                Margins vary and can be a key factor in selecting
                the right loan for you.</dd>
<dt>Market Value</dt>
<dd>The price at which a property
                will sell, assuming a knowledgeable buyer and
                seller, both operating without undue pressure.</dd>
<dt>Mortgage</dt>
<dd>A contract in which a
                borrower's property is pledged as security for a
                loan which is to be repaid on an installment
                basis.</dd>
<dt>Mortgage Note</dt>
<dd>A written promise to pay a
                debt at a stated interest rate during a specified
                term. The agreement is secured by a mortgage.</dd>
<dt>Mortgage Broker</dt>
<dd>The person who, for a
                commission or a fee, brings parties together and
                assists in negotiating contracts between them.
                Lightning Mortgage is the premier online Mortgage
                Broker.</dd>
<dt>Mortgagee</dt>
<dd>The lender in a mortgage contract.</dd>
<dt>Mortgagor</dt>
<dd>The borrower in a mortgage contract.
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="N">- N -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt><a
                class="DT" name="NegativeAmortization">Negative<br />Amortization</a></dt>
<dd>A loan in which the
                outstanding principal balance goes up instead of
                down because the monthly payments are not large
                enough to cover the full amount of interest due.
                Also called deferred interest. See our popular <a class="DD"
                href="../LoanTypes/OptionARM.php">Option ARM</a> loan programs.</dd>

				<dt><a class="DT" name="NoRatioMortgage">No Ratio Mortgage<br />Loan</a></dt>
<dd>A No Ratio Mortgage is a useful option if you are carrying more debt than a traditional mortgage will allow.
				In traditional mortgage banking your debt to income ratio is one of the key factors in determining
				loan approval. With a No Ratio Mortgage, no income information is included with the application so
				no ratio calculations are made. I use the program when a client's current income is low relative to
				their debt or, more frequently, the client has income which is impossible to verify. As all other
				information on the application is verified, the No Ratio Mortgage is a "middle ground" product
				between a Stated Income and true No Doc mortgage but may be a good choice for you. Interest only
				No Ratio Mortgage products are available.</dd>
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="O">- O -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt><a class="DT" name='Nonrecourse'>Non-recourse Loan</a></dt>
<dd>A debt instrument wherein the
	lender can only legally require repayment of a loan from the collateral or funds of the business if
	the note is not repaid. As opposed to a
	<a class="DD" href='.GlossaryQT.php#Recourse'>Recourse Loan</a> where the lender can obtain compensation from
	personal funds.</dd>
<dt>Offer to Purchase</dt>
<dd>A written proposal to buy a
                piece of real estate that becomes binding when
                accepted by the seller. Also called a sales
                contract.</dd>
<dt>Origination Fee</dt>
<dd>A fee charged for the work
                involved in the evaluation preparation and
                submission of a proposed mortgage loan.</dd>
<dt><a name="OwnerFinancing">Owner
                Financing</a></dt>
<dd>A purchase in which the seller
                provides all or part of the financing.</dd>
<dt><a name="Owner Occupied">Owner
                Occupied</a></dt>
<dd>The principal residence of the property's owner. How is this further defined?
				See Realty Times article,
				<a target="_blank"	 title="opens a new window"
				href="http://www.realtytimes.com/rtnews/printrtpages/20010129_residence.php">What's
				Your Principal Residence? Tax Experts Not Always Certain</a><img src="../images/NewWindow.gif" ALT="">
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="P">- P -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt><a class="DT" name="Periodic Cap"></a>Periodic<br />Adjustment Cap</dt>
<dd>See <a class="DD"
                href="../Answers/GlossaryAD.php#Cap">Cap
                Rates</a>.</dd>
<dt><a
                class="DT" name="PITI">PITI</a></dt>
<dd>An acronym for monthly
                payments to lender that include the principal,
                interest, taxes and insurance on a property. See <a
                class="DD"
                href="../Answers/GlossaryIL.php#Impound%20Account">Impound Account</a>.</dd>
<dt>Plat</dt>
<dd>A map of a piece of land
                showing boundary lines, streets, actual
                measurements and easements.</dd>
<dt><a
                class="DT" name="Point">Point</a>s</dt>
<dd>A fee paid to the lender on
                closing day to increase the effective yield of
                the mortgage. A point is one percent of the
                amount of the mortgage loan. Also called a
                discount point. More information about Interest
                Rate <a
                href="http://www.lightning-mortgage.com/InterestRates/InterestRateSweetSpot.php"
                class="DD">Sweet Spot</a>.</dd>
<dt>Prepayment<br />Penalty</dt>
<dd>A charge paid to the lender by
                the borrower if a mortgage loan is repaid before
                its term is over. Often abbreviated PPP.</dd>
<dt>Pre-Approval</dt>
<dd>A commitment by a lender to
                extend credit provided that specific conditions
                are met. This is not a guarantee. See <a
                class="DD"
                href="http://www.lightning-mortgage.com/Answers/PreAnswers.php">Pre-Approval
                vs. Pre-Qualification</a> for more information.</dd>
<dt>Pre-Qualification</dt>
<dd>A preliminary assessment of a
                buyer's ability to secure a loan, based on a
                specific set of lending guidelines and buyer
                representations made. This is not a guarantee or
                commitment by a lender to extend credit. See <a
                class="DD"
                href="http://www.lightning-mortgage.com/Answers/PreAnswers.php">Pre-Approval
                vs. Pre-Qualification</a> for more information.</dd>
<dt>Prime Rate</dt>
<dd>The interest rate charged by
                banks to their preferred corporate customers, it
                tends to be an estimator for general trends in
                short term interest rates.</dd>
<dt>Principal</dt>
<dd>The amount of money borrowed
                or remaining unpaid; also, that part of the
                monthly payment that reduces the outstanding
                balance of a mortgage.</dd>
<dt><a
                class="DT" name="PMI">PMI (Private<br />Mortgage<br />Insurance)</a></dt>
<dd>Insurance written by a private
                company to protect the lender against loss caused
                by mortgage default. More detail is <a
                class="DD"
                href="../Answers/PMIAnswers.php">here</a>.</dd>
