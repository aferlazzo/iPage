<dl compact>
<dd><a style="font:x-large Times New Roman;margin-right:10em;" name="Q">- Q -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt>Qualifying Ratios</dt>
<dd>Guidelines applied by lenders
                to determine how large a loan to grant a home
                buyer. See <a class="DD"
                href="./GlossaryAD.php#DebtToIncome">DTI</a> and <a
                class="DD"
                href="./GlossaryIL.php#Loan-To-Value Ratio">LTV</a>.
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="R">- R -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt><a class="DT" name="Rate Lock">Rate Lock</a></dt>
<dd>A rate lock is a contractual
                agreement between the lender and borrower. There
                are four components to a rate lock: loan program,
                interest rate, points, and the time-length of the
                lock.</dd>
<dt><a  class="DT" name="Real-Estate-Settlement-Statement">Real
                Estate Settlement Statement</a></dt>
<dd>Final settlement statement
                often referred to as the <a class="DD"
                href=".GlossaryEH.php#HUD-1 Form">HUD-1</a>, form
                used to itemize buyer, seller, broker, and lender
                charges and credits at closing. A line-by-line <a
                class="DD"
                href="../Answers/HUD1Explained.php">explanation</a>.</dd>
<dt>Realtor &reg;</dt>
<dd>A real estate broker or sales
                associate affiliated with the National
                Association of Realtors. We at Lightning Mortgage are
                not Realtors, but are licensed Mortgage Brokers.</dd>
<dt>Recording Fee</dt>
<dd>The charges made by the
                register of deeds to record the legal documents.</dd>
<dt><a class="DT" name='Recourse'>Recourse Loan</a></dt>
<dd>A debt instrument wherein the 
	lender can legally require repayment of a loan from personal funds if 
	the collateral is not sufficient to repay the note. As opposed to a 
	<a class="DD" href='.GlossaryMP.php#Nonrecourse'>Non-recourse Loan</a> where the lender can only obtain compensation from 
	the collateral or funds of the business.</dd>
<dt>Refinancing</dt>
<dd>Repaying a debt with the
                proceeds of a new loan, using the same property
                as collateral or security.</dd>
<dt>Residential Mortgage
                Credit Report (<a class="DT" name="RMCR">RMCR)</a></dt>
<dd>A credit report required by
                lenders and the secondary market to establish the
                credit worthiness of a borrower. The report is a
                merged credit information provided by Experian,
                Equifax, and TransUnion, just like a tri-bureau
                Infile report. In addition, the RMCR report is
                audited to remove information like accounts
                belonging to a borrower with a different social
                security number.
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="S">- S -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt>Second Mortgage</dt>
<dd>A loan on property which
                already has an existing mortgage (the first
                mortgage). The second mortgage is subordinate to
                the first.</dd>
<dt>Secondary Mortgage Market</dt>
<dd>The buying and selling of
                existing mortgages through agencies (i.e. Fannie
                Mae, Freddie Mac).</dd>
<dt>Settlement</dt>
<dd>See <a class="DT"
                href="../Answers/GlossaryAD.php#Closing">'Closing.'</a></dd>

<dt><a class="DT" name="Stated Income Loan">Stated Income Loan</a></dt>
<dd>Loans that rely more on credit
                scores than on documented income for loan
                approval. These loans typically carry a higher
                interest rate than documented income. See <a
                class="DD"
                href="http://www.lightning-mortgage.com/LoanTypes/IncomeDocVariations.php">Income
                Reporting Options</a> for further information.</dd>
<dt>Survey</dt>
<dd>A map prepared by an engineer
                or surveyor charting a particular piece of real
                estate.
<br /><br /><a style="font:x-large Times New Roman;margin-right:10em;" name="T">- T -</a>
<a class="DD" href="#Top">Back to Top</a></dd>
<dt><a name="T-Bill"></a>T-Bill Index</dt>
<dd>The One Year Treasury Bill. An <a
                class="DD" href="../Answers/GlossaryIL.php#Index">index</a> published
                by the Federal Reserve Board based on the average
                yield of a range of Treasury securities, all
                adjusted to the equivalent of a one-year
                maturity. It is used to set the cost of
                adjustable-rate mortgages (<a class="DD"
                href="../LoanTypes/ARM.php">ARMs</a>).&nbsp;</dd>
<dt>Title</dt>
<dd>Ownership of a property. A
                clear title is one without any outstanding liens
                or encumbrances. A 'cloud on title' refers to any
                outstanding liens or encumbrances which could
                impair the title. There are 6 different ways of <a
                class="DD"
                href="http://www.lightning-mortgage.com/Answers/HoldingTitle.php">holding
                title</a>.</dd>
<dt>Title Insurance Policy</dt>
<dd>Protection against financial
                loss arising from defects in the title occurring
                before purchase.</dd>
<dt>Title Search</dt>
<dd>A check of public record to
                disclose the past and current facts regarding
                ownership of a particular piece of property.</dd>
<dt>Transfer Tax</dt>
<dd>In some areas city, county or
                state taxes imposed when property passes from one
                person to another.</dd>
<dt>Truth-In-Lending</dt>
<dd>A federal law that requires
                lenders to fully disclose, in writing, the terms
                and conditions of a mortgage, including the APR
                and other charges.</dd>
</dl>
