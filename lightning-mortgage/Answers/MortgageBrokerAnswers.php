<h2 style='width:288px;'>What is a Mortgage Broker?</h2>
<b class="bottom"><b class="b4"></b><b class="b3"></b><b class="b2"></b><b class="b1"></b></b></div>

	<p>A mortgage broker is an independent real estate
	financing professional who specializes in the origination of
	residential mortgage loans. Mortgage brokers normally pass the actual
	funding and servicing of loans on to &quot;Wholesale Lending
	Sources.&quot; As your mortgage broker I'll combine my professional
	expertise with direct access to hundreds of loan products. This is
	how I provide the most efficient way to obtain financing tailored to your
	specific financial goals.</p>

