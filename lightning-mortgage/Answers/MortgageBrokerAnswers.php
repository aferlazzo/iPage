<h2 style='width:678px;'>What's the Difference between a Mortgage Broker and a Direct Lender?</h2>
<b class="bottom"><b class="b4"></b><b class="b3"></b><b class="b2"></b><b class="b1"></b></b></div>

	<p>As your mortgage broker, I will counsel you on the
	loans available from different lenders, take your application, and
	process the loan. A lender has only a small number of loan programs. This
	excludes many borrowers. As mortgage broker, I deal with hundreds of
	lenders. I can find the lenders who specialize in various market niches
	that many other lenders avoid, such as loans to applicants with poor
	credit ratings, loans to borrowers who do not intend to occupy the
	property, loans with minimal or no down payment, and so on.</p>

<div style='width:530px;margin:0 auto;'>
<b class="top"><b class="b1"></b><b class="b2"></b><b class="b3"></b><b class="b4"></b></b>
<h2 style='width:528px;'>Will I Save Money going directly to a Mortgage Lender?</h2>
<b class="bottom"><b class="b4"></b><b class="b3"></b><b class="b2"></b><b class="b1"></b></b></div>

	<p>Unless you are a very astute shopper who is willing to
	contact hundreds of lenders and enjoys devoting a lot of time to the
	task of evaluating different mortgage programs, you will probably do better
	dealing with a mortgage broker. Mortgage brokers do not add any net
	cost to the lending process, because they perform functions that would
	otherwise have to be done by employees of the lender. Furthermore,
	because mortgage brokers deal with multiple lenders -- in a typical
	case, 25 to 30, sometimes more -- they can shop for the best terms
	available on any given day.&nbsp;Lightning Mortgage deals
	with over 130 lenders.</p>

