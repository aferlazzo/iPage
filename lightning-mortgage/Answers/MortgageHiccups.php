	<p>You may think, like many people, that your loan will be a breeze. Most people
	believe that having great credit makes a loan "easy" to close. Others say that a small loan amount
	make the loan easier. Or a large one. Actually, having great credit or any size of loan amount has
	little impact on how much work is involved with closing an escrow.</p>

	<p>What gives us the challenge are encountering some unforeseen hiccup along the way
	to closing. Here are some of the most common "hiccups" that cause submission problems, delays, and
	add expense to closing:</p>

	<ol>
		<li><p>You may not being clear on what exactly you are "buying" or not be fully
		committed and later delay or halt a step in the process (like completing the appraisal).</p></li>

		<li><p>You may not being clear on what verifications (employment, assets, etc.)
		the loan process entails.</p></li>

		<li><p>Not providing all the proper documentation we ask you for up front.</p></li>

		<li><p>Your spouse might not be fully "sold" on the benefits of the transaction
		and later decides not to go through with purchasing or refinancing.</p></li>

		<li><p>You may still shopping interest rates, even though we are well into the
		process.</p></li>

		<li><p>Not returning phone calls from us, or other third parties involved
		in the transaction (such as appraisers, underwriters, lawyers, etc.).</p></li>
		<li><p>Real estate agents and others occasionally foul-up the process by playing
		sides, sometimes even making mortgage brokers look like the "bad guy" just to win points!</p></li>

		<li><p>Not being clear about whether or not you will be escrowing just taxes,
		insurance, or both.</p></li>

		<li><p>Not being clear about the amount of money which will be needed to be
		escrowed per the investment institution's policies.</p></li>

		<li><p>If refinancing, not being clear that the payoff amount will be slightly
		different than what the balance on your statement. The reason for the difference has to do with
		pre-paid interest and mortgage payments done in arrears.</p></li>
		<li><p>Finding out about a secondary lien on the property or an open HELOC,
		late in the process.</p></ol>
</div><div class="RC" id="right"><ol start="12">

		<li><p>You continue to spend large sums of money, open accounts, and move
		finances around.</p></li>

		<li><p>The subject property does not appraise high enough.</p></li>

		<li><p>Appraisal comparable properties are too old or too far away for the
		underwriter to accept.</p></li>

		<li><p>Not closing the loan prior to the rate-lock expiration date.</p></li>

		<li><p>Repairs or remodeling that are currently being done on the property.</p></li>

		<li><p>Finding out about financial skeletons in your past which weren't disclosed
		up front, such as child support payments, wage garnishments, liens, or other open debt not
		showing on credit report.</p></li>

		<li><p>You forget to make mortgage payments or other loan payments during your
		loan transaction.</p></li>

		<li><p>On purchase transactions, when selling one property and buying another
		one, existing property is having problems with the sale, thus stalling our transaction.</p></li>

		<li><p>You go on vacation or are not available to sign within the 30-45 day
		window of the transaction.</p></li>

		<li><p>Underwriter keeps questioning the same loan conditions over and over
		again.</p></li>

		<li><p>Mortgage payments have accidentally been "double-debited" on the loan
		application, throwing ratios out of whack.</p></li>

		<li><p>HELOC subordination letter is holding-up the process, secondary lien
		holder refusing to cooperate.</p></li>

		<li><p>Not submitting proper paperwork to the borrower, investor, or other
		third party.</p></li>

		<li><p>Not verifying a full and complete application at the beginning.  Numbers
		are not accurate or facts left off the application.</p></li>

		<li><p>Closing attorney adds fees to the HUD-1 which the borrower isn't aware of
		at the last minute.</p></li>

		<li><p>The HUD-1 closing statement is incorrect.</p></li>

		<li><p>Borrowers get lost, change their mind, pull-out, or just simply fail to
		show-up at the closing. Believe me, it happens.</p></li>
	</ol>
