	<p>The Annual Percentage Rate (APR) is the <i>cost</i>
	of your credit expressed as an annual rate. This is not necessarily equal
	to the loan interest rate! Click <a class="DD"
	href="http://www.lightning-mortgage.com/Answers/APRAnswers.php">APR, Revealed</a>
	for a more detailed description of APR and its relationship to your mortgage's interest rate.</p>

	<p>The Finance Charge is the cost of credit expressed in
	dollars. It is the total amount of interest calculated at the interest
	rate over the life of the loan, plus Prepaid Finance Charges and the total
	amount of any required <a class="DD"
	href="GlossaryMP.php#PMI">mortgage insurance</a> charged over the life
	of the loan. This would cost only if you kept the loan for the entire loan
	term.</p>

	<p>The Amount Financed is the loan amount applied
	for, plus or minus the Prepaid 	Finance Charges. When purchasing,
	Prepaid Finance Charges include items paid at or before settlement, such as
	loan origination, commitment or discount fees ('points'),
	adjusted interest, and initial mortgage insurance premium. The
	Amount Financed is lower than the amount you applied for because it
	represents a NET figure. If you applied for $150,000 and the Prepaid
	Finance Charges total $12,000, the Amount Financed would be $138,000.</p>

	<p>When refinancing, one normally includes Prepaid
	Finance&nbsp; Charges in the loan amount over and above the cost to
	payoff ant existing loans on the property.</p>

	<p>This figure represents the total amount you will
	have paid if you make the minimum required payments for the entire term
	of the loan. This includes principal, interest, and mortgage insurance
	premiums, but does not include payments for real estate taxes or
	property insurance premiums.</p>
