	<p>But wait! Just as you're preparing to breathe a sign of
	relief, you receive a <i>Truth In Lending</i> document. Yes, it's
	yet another disclosure, another standard form meant to provide you
	with more information. The Disclosure is designed to give you information
	about the costs of your loan so that you may compare these costs with
	those of other loan programs or lenders.</p>
	<p>Good intentions aside, the document often causes more
	questions than providing answers. But luckily you're working with us at
	Lightning Mortgage. We'll explain this very confusing document. The
