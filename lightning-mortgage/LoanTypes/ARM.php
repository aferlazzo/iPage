	lower the cost. Here's the scoop of adjustable rate mortgages.</p>
<blockquote class="PullQuote" style="float:left;margin:0 1em 0 0;">
     The <a href="./OptionARM.php">Option ARM</a> is an alternative that combines the best of fixed-rate and adjustable rate mortgages,
</blockquote>

	<p>While we have your classic 1- or 6-month adjustable rate mortgage product,
	a new breed of hybrid loans give many people lower interest rates
	that are fixed for an initial period of time, without the fire-drill exercise that people sometimes
	go through with <a href="../LoanTypes/Balloon.php">balloon</a> mortgages.</p>
	<p>With these hybrid loans both the
	interest rate and monthly payment remain the same for a fixed period. The most common fixed
	periods are 1, 3, 5, 7, and 10 years. Unlike a balloon mortgage, at the end
	of the fixed period the rate begins to float, but can only rise or fall at fixed
	intervals. </p>

	<p>The <a href="../Answers/GlossaryAD.php#Cap">amount the
	rate can change</a> is predetermined (normally ½% to 2% per upward or downward change). The
	intervals are normally 6 or 12 months. Typically there is a <a
	class="LoanTypes" href="../Answers/GlossaryAD.php#Cap">&quot;Cap&quot;</a>
	on the margin, which determines the highest the rate could ever go.</p>

	<ul>
		<li>Obtain a  lower rate, for a known
			period of time, while you watch the market to see if
			and when fixed rates get better</li>
		<li>Balloon mortgages may have a slightly
			better rate than fixed rate mortgages, but an ARM will adjust at the
			end of the &quot;fixed period&quot;, whereas a
			&quot;Balloon&quot; has to be refinanced or paid in full</li>
		<li>Recommended if staying in the home for a
			fixed period and have taken the time to factor in the
			margin, to determine that they would not be better off
			with a Fixed Balloon or even a Fixed Rate</li>
		<li>Very attractive to borrowers who are in the process of cleaning up their credit</li>
	</ul>

