	<p>Both the rate
	and payment remain the same for a
	short fixed period, at the end of
	which, the rate and payment increase.
	The rate and payment may increase
	once, twice, or even three times,
	depending on whether the buy down is
	a 1/1, 2/1, or 3/1. The percentage of
	increase, as well as number of
	increases is predetermined. Once all
	of the increases have occurred the
	new rate and payment remain fixed for
	the term of the loan.</p>

	<p>Lenders will
	typically charge a fee to &quot;buy
	the rate down&quot; for the first 1,
	2, or 3 years of the loan. The
	advantage to a Buy down loan is that
	it offers a lower rate and payment
	during the first few years of the
	loan. Buy downs are recommended for
	those borrowers who are having
	trouble qualifying from an income
	stand point.</p>

	<p>If a builder
	offered to provide the buyer funds
	for a &quot;2-1 Buy down&quot; on a
	7.50% loan, would mean that payments
	are made by the borrower as follows:</p>
	<ul>
		<li>First 12 months: Payments at 5.50% interest</li>
		<li>Second 12 months: Payments at 6.50% interest</li>
		<li>Remaining Term: Payments at 7.50% interest</li>
	</ul>

