	<p>Simply put, an interest-only mortgage doesn't
	require any principal payment. Your monthly payment is just interest. So
	there's no equity build-up through making payments. The downside is if
	real estate prices &quot;crash.&quot; Then you are &quot;upside-down&quot;
	until the recovery. And there's always a recovery, by the way. If history continues
	to repeat itself, it is always just a matter of time before prices go up.</p>
	<p>Without paying down the principal, you wouldn't own your home
	free-and-clear if you were to own it indefinitely. However, you're not
	generally penalized for making extra payments, so you can pay down the
	principal as you are able, using the &quot;interest-only&quot; option as a
	safety net.</p>

	<p>The upside is that your monthly cost is normally a
	few hundred dollars lower (depending on the size of the mortgage, of
	course). Equity is built up as the market increases, as it most often
	does. For landlords, cash flow out is lowered, with the tenants time
	renting builds equity. The best part is that you always have the option of
	making payments to your principal balance. You are in complete control over
	when and how much you want to pay down the balance. This makes it a great option for people
	who want more control on their cash flow.</p>
