<p>S.W.I.F.T. ("Society for Worldwide Inter-bank Financial Telecommunication") provides mechanisms for electronic inter-bank messaging and funds transfer. SWIFT messages are used legitimately to conduct business between transactional partners.</p>
<p>An account with the SWIFT MT999 capability allows bank-to-bank SWIFT electronic verification for Proof of Funds in the account, with no further comment. This SWIFT format is an appropriate message for a simple Proof of Funds communication and it is part of the Common Group of SWIFT formats providing a General Free Format for SWIFT messaging.</p>
<p>MT799 is a simple text message, sent bank to bank. This is used for a bank to bank proof of funds, only. The MT799 is not a form of payment and it is not a bank undertaking or promise to pay. It is simply a bank-to-bank confirmation of the funds on deposit, nothing more.</p>
<p>An account with the SWIFT MT799 capability allows bank-to-bank SWIFT electronic verification for Proof of Funds in compliance with the SWIFT Category 7 "Treasury Markets &amp; Syndication" message types. Often there is a misconception that a particular circumstance requires a SWIFT MT760 message, when in fact, the SWIFT MT799 format provides the required bank confirmation for the application.</p>

<p>An account with the SWIFT MT760 capability allows bank-to-bank SWIFT electronic verification of the account, and will include a blocked funds provision. There is no other bank guarantee provision available in the SWIFT instructions.</p>
<p>SWIFT MT542 messages are related to a SKR (Safekeeping Receipt) and related collateral. Our POF service does not accomodate these message types.</p>

